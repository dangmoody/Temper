{
	float3x3 inversed = float3x3_inverse( mat );
	TEMPER_CHECK_TRUE( float3x3_equals( &inversed, expectedAnswer ) );

	// test inversed matrix multiplied by the original equals identity
	float3x3 identityMat;
	float3x3_identity( &identityMat );

	float3x3 matMultipliedByItsInverse = float3x3_mulm( mat, &inversed );
	TEMPER_CHECK_TRUE( float3x3_equals( &matMultipliedByItsInverse, &identityMat ) );
}

